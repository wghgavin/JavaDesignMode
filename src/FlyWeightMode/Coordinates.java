package FlyWeightMode;

/**
 * 给围棋亨元增加外部状态
 */
public class Coordinates {
    private int x;
    private int y;
    public Coordinates(){
        this.x=x;
        this.y=y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
